def contract_proton_zero_momentum(propagator, latt_info, t_src, global_lt):
    _, cp, contract, _, gamma, _ = _load_runtime_modules()
    c_matrix = gamma.gamma(2) @ gamma.gamma(8)
    gamma0 = gamma.gamma(0)
    gamma4 = gamma.gamma(8)
    gamma5 = gamma.gamma(15)
    half_t = global_lt // 2
    parity_projector = cp.zeros((global_lt, 4, 4), "<c16")
    parity_projector[:half_t] = (gamma0 + gamma4) / 2
    parity_projector[half_t:] = (gamma0 - gamma4) / 2
    temporal_sign = cp.ones((2 * global_lt,), "<f8")
    temporal_sign[:] = -1
    temporal_sign[half_t : half_t + global_lt] = 1

    projector_local = cp.roll(parity_projector, t_src, 0)[latt_info.gt * latt_info.Lt : (latt_info.gt + 1) * latt_info.Lt]
    sign_local = temporal_sign[global_lt - t_src : 2 * global_lt - t_src][latt_info.gt * latt_info.Lt : (latt_info.gt + 1) * latt_info.Lt]
    proton = cp.zeros((latt_info.Lt,), "<c16")
    proton_data = propagator.data
    for a, b, c in permutations(tuple(range(3))):
        for d, e, f in permutations(tuple(range(3))):
            sign = 1 if b == (a + 1) % 3 else -1
            sign *= 1 if e == (d + 1) % 3 else -1
            proton += (sign * sign_local) * (
                contract(
                    "ij,kl,tmn,wtzyxik,wtzyxjl,wtzyxmn->t",
                    c_matrix @ gamma5,
                    c_matrix @ gamma5,
                    projector_local,
                    proton_data[:, :, :, :, :, :, :, a, d],
                    proton_data[:, :, :, :, :, :, :, b, e],
                    proton_data[:, :, :, :, :, :, :, c, f],
                )
                + contract(
                    "ij,kl,tmn,wtzyxik,wtzyxjn,wtzyxml->t",
                    c_matrix @ gamma5,
                    c_matrix @ gamma5,
                    projector_local,
                    proton_data[:, :, :, :, :, :, :, a, d],
                    proton_data[:, :, :, :, :, :, :, b, e],
                    proton_data[:, :, :, :, :, :, :, c, f],
                )
            )
    return proton


