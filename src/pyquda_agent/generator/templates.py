    vol = latt_info.volume
    nc = 3
    ns = 4
    tmp = cp.einsum(
        "ij,xkjba,kl,xliba->x",
        gamma5 @ gamma5,
        propagator.data.reshape(vol, ns, ns, nc, nc).conj(),
        gamma5 @ gamma5,
        propagator.data.reshape(vol, ns, ns, nc, nc),
        optimize=True,
    )
    correlator = cp.einsum(
        "wtzyx->t",
        tmp.reshape(2, latt_info.Lt, latt_info.Lz, latt_info.Ly, latt_info.Lx // 2),
        optimize=True,
    )
    return cp.roll(correlator, -t_src)


